"""
인보이스 분석 프롬프트
"""

def get_invoice_prompt(text: str) -> str:
    """인보이스 분석 프롬프트 생성"""
    base_prompt = f"""
다음은 인보이스 문서입니다. 이는 개별 문서이므로 해당 문서에서만 정보를 추출해주세요.

문서 내용:
{text}

"""
    
    return base_prompt + """
이 인보이스에서 **다음 12개 필드만** 정확히 추출해주세요.

**중요: 여러 개의 인보이스가 있는 경우**
- Invoice No. 또는 송품장 번호가 다른 인보이스는 각각 개별 문서로 취급해주세요
- 각 인보이스마다 페이지 번호를 반드시 포함해주세요
- 배열 형태로 각 개별 인보이스의 정보를 모두 출력해주세요

**추출할 필드 (12개만):**
1. 업체명: 페이지 상단의 회사명 ((주), 주식회사 등)
2. 일자: 일자, date 등 확인
3. 송품장번호: Invoice No. 확인 (문서 식별자)
4. B/L번호: B.L No., B/L No. 등 확인
5. 출발지: P.O.L 확인
6. 도착지: P.O.D 확인
7. 총중량: WEIGHT 확인 (존재하는 경우)
8. 컨테이너번호: Container No. 등 확인 (존재하는 경우)
9. 페이지번호: 해당 문서가 있는 페이지 번호
10. 품목정보: 테이블에서 품목명, 원화공급가, VAT 추출 (배열)
11. 공급가액Total: Total(합계) 금액 중 Amount(KRW) 확인
12. 부가세액Total: Total(합계) 금액 중 부가세 VAT(KRW) 확인

**중요한 지침:**
1. **위 12개 필드만 추출하고, 다른 정보는 절대 포함하지 마세요**
2. Invoice No. 또는 송품장 번호가 다른 인보이스는 각각 개별 문서로 처리
3. 각 문서마다 페이지 번호를 반드시 포함
4. 모든 금액은 KRW(원화) 기준으로 추출
5. 존재하지 않는 정보는 "정보 없음"으로 표시
6. **요청하지 않은 필드는 절대 추가하지 마세요**
7. **문서에 있는 다른 모든 정보는 무시하고 위 12개 필드만 추출하세요**
8. **추가 설명이나 주석 없이 순수한 JSON 배열만 반환하세요**
9. **각 필드명은 정확히 위에 명시된 대로 사용하세요**""" 