def get_transfer_receipt_prompt(text: str, file_name: str = "", page_number: int = None) -> str:
    """이체확인증 분석 프롬프트 생성"""
    base_prompt = f"""
다음은 이체확인증/송금증 문서입니다. 이는 개별 문서이므로 해당 문서에서만 정보를 추출해주세요.

문서 내용:
{text}

"""
    
    # 페이지 번호 정보 추가
    page_info = f"**현재 페이지 번호: {page_number}**" if page_number is not None else ""
    
    return base_prompt + f"""
이 이체확인증/송금증에서 **다음 5개 필드만(다른 필드는 추출하면 안됨)** 정확히 추출해주세요.

{page_info}

**중요: 여러 개의 이체확인증이 있는 경우**
- 거래일시 + 금액 + 업체명 조합이 다른 이체확인증은 각각 개별 문서로 취급해주세요
- 각 이체확인증마다 페이지 번호를 반드시 포함해주세요
- 배열 형태로 각 개별 이체확인증의 정보를 모두 출력해주세요

**추출할 필드 (5개만):**
1. 금액: 이체금액, 출금금액, 출금액, 출금의뢰금액 등에서 추출
2. 업체명: 받는분, 받으시는분, 입금계좌명, 예금주, 적요 등에서 추출
3. 일자: 거래일시, 이체일시, 이체일자, 입금일시, 처리완료일자 등에서 추출
4. 파일명: "{file_name}"
5. 페이지번호: {page_number if page_number is not None else "실제 파일의 페이지 번호 (예: 1, 2, 3...)"}

**업체명 추출 시 매우 중요한 지침:**
1. **문서에 나타난 업체명을 한 글자씩 정확히 읽어서 그대로 복사하세요**
2. **절대 유추하거나 보정하거나 예상하지 마세요. 문맥 상 유사한 업체명이라도 문서에 적힌 글자가 아니면 절대 쓰지 마세요**
3. **한글, 영어, 숫자, 특수문자 모두 정확히 복사하세요**
4. **업체명이 여러 곳에 나타나면 가장 명확하게 기록된 것을 선택하세요**
5. **괄호 (), 주식회사, (주), 유한회사, (유) 등도 정확히 포함하세요**
6. **업체명이 불분명하거나 읽기 어려운 경우 "정보 없음"으로 표시하세요**
7. **업체명 앞뒤 공백은 제거하되, 중간 공백은 유지하세요**
8. 업체명은 반드시 **한 글자씩 시각적으로 구분하여 복사**하세요.  
   OCR 결과나 예상 텍스트가 아닌, 이미지에서 직접 한 글자씩 확인하세요.

**기타 중요한 지침:**
1. **위 5개 필드(금액, 업체명, 일자, 파일명, 페이지번호)만 추출하고, 다른 데이터는 어떤 것도 절대 절대 포함하지 마세요**
   분석결과의 필드명은 반드시! 금액, 업체명, 일자, 파일명, 페이지번호 로 써주세요.
2. 거래일시 + 금액 + 업체명 조합이 다른 이체확인증은 각각 개별 문서로 처리
3. 각 문서마다 페이지 번호를 반드시 포함 (실제 파일의 페이지 번호)
4. 금액은 숫자 형식으로 정리 (예: 1,000,000원)
5. 일자는 YYYY-MM-DD 형식으로 정리
6. 존재하지 않는 정보는 "정보 없음"으로 표시
7. **요청하지 않은 필드는 절대 추가하지 마세요**
8. **문서에 있는 다른 모든 정보는 무시하고 위 5개 필드만 추출하세요**
9. **추가 설명이나 주석 없이 순수한 JSON 배열만 반환하세요**
10. **각 필드명은 정확히 위에 명시된 대로 사용하세요**

**업체명 정확도 체크리스트:**
- [ ] 문서에서 업체명 부분을 찾았는가?
- [ ] 한 글자씩 정확히 읽어서 확인했는가?
- [ ] 특수문자나 괄호도 정확히 포함했는가?
- [ ] 유추나 추측 없이 문서 그대로 복사했는가?
- [ ] 앞뒤 공백을 제거했는가?

JSON 형태로 정리해주세요. 예시:
[
{{
  "금액": "1,000,000원",
  "업체명": "(주)받는회사",
    "일자": "2024-01-01",
    "파일명": "이체확인증_20240101.pdf",
    "페이지번호": "1"
  }}
]"""