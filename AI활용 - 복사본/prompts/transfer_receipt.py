"""
이체확인증 분석 프롬프트
"""

def get_transfer_receipt_prompt(text: str) -> str:
    """이체확인증 분석 프롬프트 생성"""
    base_prompt = f"""
다음은 이체확인증/송금증 문서입니다. 이는 개별 문서이므로 해당 문서에서만 정보를 추출해주세요.

문서 내용:
{text}

"""
    
    return base_prompt + """
이 이체확인증/송금증에서 **다음 4개 필드만** 정확히 추출해주세요.

**중요: 여러 개의 이체확인증이 있는 경우**
- 거래일시 + 금액 + 업체명 조합이 다른 이체확인증은 각각 개별 문서로 취급해주세요
- 각 이체확인증마다 페이지 번호를 반드시 포함해주세요
- 배열 형태로 각 개별 이체확인증의 정보를 모두 출력해주세요

**추출할 필드 (4개만):**
1. 금액: 이체금액, 출금금액, 출금액, 출금의뢰금액 등에서 추출
2. 업체명: 받는분, 받으시는분, 입금계좌명, 예금주, 적요 등에서 추출
3. 일자: 거래일시, 이체일시, 이체일자, 입금일시, 처리완료일자 등에서 추출
4. 페이지번호: 해당 문서가 있는 페이지 번호

**중요한 지침:**
1. **위 4개 필드만 추출하고, 다른 정보는 절대 포함하지 마세요**
2. 거래일시 + 금액 + 업체명 조합이 다른 이체확인증은 각각 개별 문서로 처리
3. 각 문서마다 페이지 번호를 반드시 포함
4. 금액은 숫자 형식으로 정리 (예: 1,000,000원)
5. 일자는 YYYY-MM-DD 형식으로 정리
6. 존재하지 않는 정보는 "정보 없음"으로 표시
7. **요청하지 않은 필드는 절대 추가하지 마세요**
8. **문서에 있는 다른 모든 정보는 무시하고 위 4개 필드만 추출하세요**
9. **추가 설명이나 주석 없이 순수한 JSON 배열만 반환하세요**
10. **각 필드명은 정확히 위에 명시된 대로 사용하세요**

JSON 형태로 정리해주세요. 예시:
[
{
  "금액": "1,000,000원",
  "업체명": "(주)받는회사",
    "일자": "2024-01-01",
    "페이지번호": 1
  }
]""" 